package lu.ing.gameofcode.line;

public class LineBean {

    private String num;
    private String name;
    private String catIn;
    private String latitude;
    private String longitude;
    private String stopId;
    private String stopExtId;

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCatIn() {
        return catIn;
    }

    public void setCatIn(String catIn) {
        this.catIn = catIn;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getStopId() {
        return stopId;
    }

    public void setStopId(String stopId) {
        this.stopId = stopId;
    }

    public String getStopExtId() {
        return stopExtId;
    }

    public void setStopExtId(String stopExtId) {
        this.stopExtId = stopExtId;
    }
}